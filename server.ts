import express from 'express';
import { createServer as createViteServer } from 'vite';
import path from 'path';
import { fileURLToPath } from 'url';
import { GoogleGenAI } from '@google/genai';
import dotenv from 'dotenv';

dotenv.config();

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

async function startServer() {
  const app = express();
  const PORT = 3000;

  app.use(express.json());

  // Contact Form API (Optional: could integrate with an email service here)
  app.post('/api/contact', async (req, res) => {
    try {
      const data = req.body;
      console.log('New Contact Inquiry:', data);
      // In a real scenario, you'd use something like Nodemailer or SendGrid here
      // to send an email to britcam.in@gmail.com
      res.json({ success: true, message: 'Inquiry logged on server' });
    } catch (error) {
      res.status(500).json({ error: 'Failed to log inquiry' });
    }
  });

  if (process.env.NODE_ENV !== 'production') {
    const vite = await createViteServer({
      server: { middlewareMode: true },
      appType: 'spa',
    });
    app.use(vite.middlewares);
  } else {
    const distPath = path.join(process.cwd(), 'dist');
    app.use(express.static(distPath));
    app.get('*', (req, res) => {
      res.sendFile(path.join(distPath, 'index.html'));
    });
  }

  app.listen(PORT, '0.0.0.0', () => {
    console.log(`Server running at http://localhost:${PORT}`);
  });
}

startServer();
